# Function to count the number of characters
def count_characters(text):
    return len(text)

# Example usage
text = """Khwopa Engineering College, 8, chyamasingh, Bhaktapur-09, भक्तपुर, भक्तपुर नगरपालिका, भक्तपुर, बागमती प्रदेश, 89130, नेपाल"""
print("Number of characters:", count_characters(text))
